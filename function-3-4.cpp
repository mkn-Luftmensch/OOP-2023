        case 'A':
            cout << "Pass" << endl;
            break;
        case 'B':
            cout << "Pass" << endl;
            break;
        case 'C':
            cout << "Pass" << endl;
            break;
        case 'D':
            cout << "Fail" << endl;
            break;
        case 'E':
            cout << "Fail" << endl;
            break;
